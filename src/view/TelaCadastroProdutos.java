    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Produto pr = new Produto();           
        n = Double.parseDouble(cod.getText().replaceAll(",", "."));
        pr.setNome(pnome.getText());
        pr.setCategoria(cat.getText());
        pr.setPreco(n);
        this.r.setNome(pnome.getText());
        this.r.setCategoria(cat.getText());
        this.r.setPreco(n);
        BD d = new BD();
        JOptionPane.showMessageDialog(null, pr.getPreco()+",\n foi cadastrado com SUCESSO!");

        try {
            d.criar(pr);
        } catch (SQLException ex) {
            Logger.getLogger(TelaCadastroProdutos.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        this.dispose();
        this.pro.add(pr);
        
    }//GEN-LAST:event_jButton1ActionPerformed


