    public TelaCadastrarPedidos(ListadeP produto,ListadePessoas list) {
        initComponents();
        this.pr = new ArrayList<>();
        this.funcio = new ArrayList<>();
        this.c = new ArrayList<>();
        
        this.pedid = new Pedido();
        
        
        
        
        for(Cliente cli : list.getClientes()){
            this.c.add(cli);
            this.boxCliente.addItem(cli.getNome());
        }
        for(Produto prod : produto.getP()){
            this.pr.add(prod);
            this.boxProduto.addItem(prod.getNome());
        }
         for(Funcionarios f : list.getFuncionarios()){
            this.funcio.add(f);
            this.boxFuncionario.addItem(f.getNome());
        }
    }

    public Pedido retornaPedido(){
       
        return this.pedid;
    }
