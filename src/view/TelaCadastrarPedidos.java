    private void cadastrarPActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cadastrarPActionPerformed
        this.dispose();
        for(Cliente t : c){
            if(t.getNome().equals(this.boxCliente.getSelectedItem())){
                pedid.setCliente(t);
                
            }
        }
        for(Produto t : pr){
            if(t.getNome().equals(this.boxProduto.getSelectedItem())){
                pedid.setP(t);
                
            }
        }
        
        for(Funcionarios t : funcio){
            if(t.getNome().equals(this.boxFuncionario.getSelectedItem())){
                pedid.setFunciona(t);
                
            }
        }  
        
        JOptionPane.showMessageDialog(null, "Pedido:"+pedid.getP().getNome()+",\n foi cadastrado com SUCESSO!");
        
    }//GEN-LAST:event_cadastrarPActionPerformed

